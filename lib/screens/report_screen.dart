  /// Parse report text into titled sections
  List<_ReportSection> _parseSections(String report) {
    final sections = <_ReportSection>[];
    final lines = report.split('\n');

    String? currentTitle;
    final buffer = StringBuffer();

    // Section headers are lines that are:
    // 1. Markdown headers (### TITLE)
    // 2. ALL CAPS lines
    // 3. Known keywords (EXECUTIVE, FINDINGS, ROOT CAUSE, COMPLIANCE, ACTIONS)
    final sectionHeaderRegex = RegExp(r'^(?:#+\s*)?([A-Z][A-Z \u2014\u2500—_\-:]+)$');
    // Separator lines (=== or ---)
    final separatorRegex = RegExp(r'^[=\-]{3,}$');

    for (final line in lines) {
      final trimmed = line.trim();

      // Skip separator lines
      if (separatorRegex.hasMatch(trimmed)) continue;
      // Skip empty header lines like "FAIRSIGHT AI FAIRNESS AUDIT REPORT"
      if (trimmed == 'FAIRSIGHT AI FAIRNESS AUDIT REPORT') continue;
      // Skip footer
      if (trimmed.startsWith('Generated by FairSight')) continue;

      // Clean line for checking
      String cleanLine = trimmed.replaceAll(RegExp(r'[#\*]'), '').trim();
      
      bool isHeader = (sectionHeaderRegex.hasMatch(cleanLine) && cleanLine.length > 3) ||
                      (trimmed.startsWith('###') && trimmed.length > 5) ||
                      (trimmed.startsWith('**') && trimmed.endsWith('**') && trimmed.length > 5);

      if (isHeader) {
        // Save previous section
        if (currentTitle != null) {
          sections.add(_ReportSection(currentTitle!, buffer.toString().trim()));
          buffer.clear();
        }
        currentTitle = cleanLine.replaceAll(':', '');
      } else {
        if (currentTitle != null) {
          buffer.writeln(trimmed);
        }
      }
    }

    // Save last section
    if (currentTitle != null && buffer.isNotEmpty) {
      sections.add(_ReportSection(currentTitle!, buffer.toString().trim()));
    }

    return sections;
  }

